export interface SampleData {

  /**
   * Vocabank domain name.
   *
   * @type {string}
   * @memberof SampleData
   */
  domain: string;

  /**
   * File hash ID.
   *
   * @type {string}
   * @memberof SampleData
   */
  hashId: string;

  /**
   * Vocabank URL.
   *
   * @type {string}
   * @memberof SampleData
   */
  url: string;

  /**
   * Vocabank URL for downloading.
   *
   * @type {string}
   * @memberof SampleData
   */
  downloadUrl: string;

  /**
   * Vocabank URL for listening.
   *
   * @type {string}
   * @memberof SampleData
   */
  listenUrl: string;
}
